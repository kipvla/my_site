      <p mb-5>
        In a varied career as a violist and violinist, Kip has performed with the
        Chicago, St. Louis and New World Symphonies, the Civic Orchestra of
        Chicago, and alongside Yo-Yo Ma and Tabea Zimmerman.
      </p>
      ​
      <p>
        Kip's passion for engaging audiences drives myriad personal initiatives.
        As a New World Symphony fellow, he worked with artistic director Michael
        Tilson Thomas to program, produce, and host a concert exploring the role
        of the artist in today’s world, and led 16 New World Symphony fellows in
        studying Spanish and leading public performances in which all musicians
        provided Spanish commentary.
      </p>
      ​
      <p>
        Kip has spent 10 years studying the Alexander Technique (AT), a method for
        enhancing coordination and embodied thoughtfulness which he credits for
        bringing new life to his music-making. As a Civic Orchestra of Chicago
        fellow, Kip designed and implemented The Rediscovering Musician in which
        two Civic Orchestra of Chicago colleagues received eight Alexander
        Technique lessons as he followed and documented their individual journeys.
        Kip maintains a YouTube channel about Alexander Technique.
      </p>
      ​
      <p>
        A passion for education has led him to over 15 years of teaching
        experience, from beginning level to college and professional, in English
        and in Spanish. He maintains an online Zoom violin and viola studio.
      </p>
